$days=['понеділок','вівторок','середа','четвер','п\'ятниця','субота','неділя'];
$weekend=['субота','неділя'];

foreach ($days as $d)
{
    if (in_array($d,$weekend))
    {
        echo "<b> $d </b></br>";
    }
    else
    {
        echo "$d </br>";
    }

}